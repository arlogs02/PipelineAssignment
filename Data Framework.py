    "def _aqi_category(val):\n",
    "    \"\"\"Map a numeric AQI value to its EPA category label.\"\"\"\n",
    "    if pd.isna(val):\n",
    "        return None\n",
    "    for lo, hi, label in AQI_CATEGORIES:\n",
    "        if lo <= val <= hi:\n",
    "            return label\n",
    "    return \"Hazardous\"\n",
    "\n",
    "def transform(df: pd.DataFrame) -> pd.DataFrame:\n",
    "\n",
    "    log.info(\"Transforming and deriving metrics …\")\n",
    "    df = df.copy()\n",
    "\n",
    "    df[\"aqi_category\"] = df[\"us_aqi\"].map(_aqi_category)\n",
    "\n",
    "    sub_aqi_cols = {\n",
    "        \"us_aqi_pm2_5\":            \"PM2.5\",\n",
    "        \"us_aqi_pm10\":             \"PM10\",\n",
    "        \"us_aqi_ozone\":            \"Ozone\",\n",
    "        \"us_aqi_nitrogen_dioxide\": \"NO₂\",\n",
    "        \"us_aqi_carbon_monoxide\":  \"CO\",\n",
    "        \"us_aqi_sulphur_dioxide\":  \"SO₂\",\n",
    "    }\n",
    "    present = [c for c in sub_aqi_cols if c in df.columns]\n",
    "    if present:\n",
    "        sub     = df[present].copy().astype(float)\n",
    "        all_na  = sub.isna().all(axis=1)\n",
    "        dominant = pd.Series(index=df.index, dtype=object)\n",
    "        if (~all_na).any():\n",
    "            dominant[~all_na] = (\n",
    "                sub[~all_na].idxmax(axis=1, skipna=True).map(sub_aqi_cols)\n",
    "            )\n",
    "        df[\"dominant_pollutant\"] = dominant\n",
    "\n",
    "    df[\"pm_ratio\"] = (df[\"pm2_5\"] / df[\"pm10\"].replace(0, np.nan)).round(3)\n",
    "\n",
    "    df[\"uv_cloud_attenuation_pct\"] = np.where(\n",
    "        df[\"uv_index_clear_sky\"] > 0,\n",
    "        ((df[\"uv_index_clear_sky\"] - df[\"uv_index\"])\n",
    "         / df[\"uv_index_clear_sky\"] * 100).round(1),\n",
    "        np.nan,\n",
    "    )\n",
    "\n",
    "    who_map = {\n",
    "        \"pm2_5\":            THRESHOLDS[\"pm2_5\"][\"who\"],\n",
    "        \"pm10\":             THRESHOLDS[\"pm10\"][\"who\"],\n",
    "        \"ozone\":            THRESHOLDS[\"ozone\"][\"who\"],\n",
    "        \"nitrogen_dioxide\": THRESHOLDS[\"nitrogen_dioxide\"][\"who\"],\n",
    "        \"sulphur_dioxide\":  THRESHOLDS[\"sulphur_dioxide\"][\"who\"],\n",
    "    }\n",
    "    for col, limit in who_map.items():\n",
    "        if col in df.columns:\n",
    "            df[f\"who_exceedance_{col}\"] = (df[col] > limit).astype(\"Int8\")\n",
    "\n",
    "    df[\"date\"]        = df[\"time\"].dt.date\n",
    "    df[\"hour\"]        = df[\"time\"].dt.hour\n",
    "    df[\"week\"]        = df[\"time\"].dt.isocalendar().week.astype(int)\n",
    "    df[\"day_of_week\"] = df[\"time\"].dt.day_name()\n",
    "\n",
    "    log.info(\"  → Derived columns added\")\n",
    "    return df\n",
    "\n",
    "\n",
    "# ===========================================================================\n",
    "# STEP 4 — DATA VALIDATION & QUALITY CHECKS\n",
    "# ===========================================================================\n",
    "def validate(df: pd.DataFrame, locations_df: pd.DataFrame) -> pd.DataFrame:\n",
    "\n",
    "    log.info(\"Running data validation …\")\n",
    "    issues = []\n",
    "\n",
    "    def flag(check, col, count, detail=\"\"):\n",
    "        level = \"PASS\" if count == 0 else \"FAIL\"\n",
    "        if count:\n",
    "            log.warning(\"  [QA %s] %s | column: %s | %s rows | %s\",\n",
    "                        level, check, col, f\"{count:,}\", detail)\n",
    "        else:\n",
    "            log.info(\"  [QA %s] %s\", level, check)\n",
    "        issues.append({\n",
    "            \"check\": check, \"column\": col,\n",
    "            \"failed_rows\": count, \"detail\": detail, \"status\": level,\n",
    "        })\n",
    "\n",
    "    # 1. Null timestamps\n",
    "    flag(\"null_time\", \"time\", int(df[\"time\"].isna().sum()))\n",
    "\n",
    "    # 2. Duplicate (location_id, time) pairs\n",
    "    dups = int(df.duplicated(subset=[\"location_id\", \"time\"]).sum())\n",
    "    flag(\"duplicate_location_time\", \"location_id+time\", dups)\n",
    "\n",
    "    # 3. Referential integrity — readings must reference known locations\n",
    "    valid_ids = set(locations_df[\"location_id\"].unique())\n",
    "    unknown = int((~df[\"location_id\"].isin(valid_ids)).sum())\n",
    "    flag(\"unknown_location_id\", \"location_id\", unknown)\n",
    "\n",
    "    # 4. AQI range (EPA scale: 0–500)\n",
    "    if \"us_aqi\" in df.columns:\n",
    "        oob = df[\"us_aqi\"].between(0, 500, inclusive=\"both\")\n",
    "        flag(\"aqi_out_of_range\", \"us_aqi\",\n",
    "             int((~oob & df[\"us_aqi\"].notna()).sum()), \"valid range 0–500\")\n",
    "\n",
    "    # 5. UV index range (0–20; values above 11 are Extreme)\n",
    "    if \"uv_index\" in df.columns:\n",
    "        oob = df[\"uv_index\"].between(0, 20, inclusive=\"both\")\n",
    "        flag(\"uv_out_of_range\", \"uv_index\",\n",
    "             int((~oob & df[\"uv_index\"].notna()).sum()), \"valid range 0–20\")\n",
    "\n",
    "    # 6. Negative concentrations — should be zero after clipping step\n",
    "    for col in [\"pm2_5\", \"pm10\", \"ozone\", \"carbon_monoxide\"]:\n",
    "        if col in df.columns:\n",
    "            neg = int((df[col] < 0).sum())\n",
    "            flag(\"negative_concentration\", col, neg)\n",
    "\n",
    "    # 7. High null rate per column (threshold: > 20%)\n",
    "    total = len(df)\n",
    "    for col in df.columns:\n",
    "        miss = int(df[col].isna().sum())\n",
    "        if miss / total > 0.20:\n",
    "            flag(\"high_null_rate\", col, miss, f\"{miss / total:.1%} missing\")\n",
    "\n",
    "    # 8. Temporal gaps > 1 hour within each location's time series\n",
    "    for loc_id, grp in df.groupby(\"location_id\"):\n",
    "        grp  = grp.sort_values(\"time\")\n",
    "        gaps = grp[\"time\"].diff().dt.total_seconds() / 3600\n",
    "        large = int((gaps > 1).sum())\n",
    "        if large:\n",
    "            flag(\"temporal_gap\", \"time\", large,\n",
    "                 f\"location_id={loc_id} has {large} gap(s) > 1 h\")\n",
    "\n",
    "    # 9. Row count verification — confirm expected rows per location\n",
    "    expected_rows_per_loc = 7 * 24  # 7-day forecast at hourly resolution\n",
    "    for loc_id, grp in df.groupby(\"location_id\"):\n",
    "        actual = len(grp)\n",
    "        if actual < expected_rows_per_loc:\n",
    "            flag(\"low_row_count\", \"location_id\",\n",
    "                 expected_rows_per_loc - actual,\n",
    "                 f\"location_id={loc_id}: got {actual}, expected {expected_rows_per_loc}\")\n",
    "\n",
    "    qa_df  = pd.DataFrame(issues)\n",
    "    passed = int((qa_df[\"failed_rows\"] == 0).sum())\n",
    "    failed = int((qa_df[\"failed_rows\"] > 0).sum())\n",
    "    log.info(\"  → QA complete: %d checks passed, %d with issues\", passed, failed)\n",
    "    return qa_df\n",
    "\n",
    "\n",
    "# ===========================================================================\n",
    "# STEP 5 — DATABASE LOADING (PostgreSQL) & CSV EXPORT\n",
    "    #Create hub and spoke table to postgreSQL - INCREMENTAL LOADING STARAGEGY IS THIS:\n",
    "    #Open-Meteo API returns a rolling 7-day forcast \n",
    "    #Each run will overlap with the previous and a full replace will lose any manual annotations \n",
    "\n",
    "    #Rec strategy - Append with Deduplication\n",
    "    #1 - load full incoming dataframe into temp staging table\n",
    "    #2 - delete from permanent table any rows whose loc_ID, time already exist\n",
    "    #3 insert all rows from staging into perm table\n",
    "\n",
    "    #Allows for no duplications over time, updated forcast values, and rows not present in new window remain untouched\n",
    "    #Audit table is also maintained \n",
    "# ===========================================================================\n",
    "def load_to_postgres(\n",
    "    df: pd.DataFrame,\n",
    "    locations_df: pd.DataFrame,\n",
    "    engine,\n",
    ") -> None:\n",
    "   \n",
    "    from sqlalchemy import text\n",
    "\n",
    "    log.info(\"Loading to PostgreSQL (incremental append with deduplication) …\")\n",
    "\n",
    "    run_started_at = datetime.now(timezone.utc)\n",
    "    rows_in = len(df)\n",
    "\n",
    "    try:\n",
    "        # ------------------------------------------------------------------\n",
    "        # Locations — upsert: insert new, update existing on conflict\n",
    "        # ------------------------------------------------------------------\n",
    "        with engine.begin() as conn:\n",
    "            conn.execute(text(\"\"\"\n",
    "                CREATE TABLE IF NOT EXISTS locations (\n",
    "                    location_id          INTEGER PRIMARY KEY,\n",
    "                    name                 TEXT,\n",
    "                    state                TEXT,\n",
    "                    country              TEXT,\n",
    "                    latitude             DOUBLE PRECISION,\n",
    "                    longitude            DOUBLE PRECISION,\n",
    "                    elevation            DOUBLE PRECISION,\n",
    "                    timezone             TEXT,\n",
    "                    timezone_abbreviation TEXT,\n",
    "                    utc_offset_seconds   INTEGER\n",
    "                )\n",
    "            \"\"\"))\n",
    "\n",
    "        # Stage locations then upsert\n",
    "        locations_df.to_sql(\"locations_stage\", engine,\n",
    "                            if_exists=\"replace\", index=False)\n",
    "        with engine.begin() as conn:\n",
    "            conn.execute(text(\"\"\"\n",
    "                INSERT INTO locations\n",
    "                SELECT * FROM locations_stage\n",
    "                ON CONFLICT (location_id) DO UPDATE SET\n",
    "                    name                  = EXCLUDED.name,\n",
    "                    latitude              = EXCLUDED.latitude,\n",
    "                    longitude             = EXCLUDED.longitude,\n",
    "                    elevation             = EXCLUDED.elevation,\n",
    "                    timezone              = EXCLUDED.timezone,\n",
    "                    timezone_abbreviation = EXCLUDED.timezone_abbreviation,\n",
    "                    utc_offset_seconds    = EXCLUDED.utc_offset_seconds\n",
    "            \"\"\"))\n",
    "            conn.execute(text(\"DROP TABLE IF EXISTS locations_stage\"))\n",
    "        log.info(\"  → locations: upserted %d rows\", len(locations_df))\n",
    "\n",
    "        # ------------------------------------------------------------------\n",
    "        # Build the readings hub with reading_id assigned from df index\n",
    "        # ------------------------------------------------------------------\n",
    "        df = df.copy()\n",
    "        df[\"reading_id\"]  = df.index\n",
    "        df[\"data_source\"] = \"api\"\n",
    "        df[\"created_at\"]  = run_started_at\n",
    "\n",
    "        # Helper: incremental load for a given spoke table\n",
    "        def incremental_load(table: str, cols: list[str]) -> int:\n",
    "            \"\"\"\n",
    "            Stage → delete matching keys → insert.\n",
    "            Returns the number of rows written.\n",
    "            \"\"\"\n",
    "            subset = df[[c for c in cols if c in df.columns]].copy()\n",
    "            if subset.empty:\n",
    "                return 0\n",
    "\n",
    "            stage = f\"{table}_stage\"\n",
    "            subset.to_sql(stage, engine, if_exists=\"replace\", index=False)\n",
    "\n",
    "            key_cols = [\"location_id\", \"time\"] if \"time\" in cols else [\"reading_id\"]\n",
    "            key_join = \" AND \".join(\n",
    "                f\"{table}.{k} = {stage}.{k}\" for k in key_cols\n",
    "            )\n",
    "            with engine.begin() as conn:\n",
    "                conn.execute(text(f\"\"\"\n",
    "                    CREATE TABLE IF NOT EXISTS {table}\n",
    "                    AS SELECT * FROM {stage} WHERE 1=0\n",
    "                \"\"\"))\n",
    "                conn.execute(text(f\"\"\"\n",
    "                    DELETE FROM {table}\n",
    "                    USING {stage}\n",
    "                    WHERE {key_join}\n",
    "                \"\"\"))\n",
    "                conn.execute(text(f\"INSERT INTO {table} SELECT * FROM {stage}\"))\n",
    "                conn.execute(text(f\"DROP TABLE IF EXISTS {stage}\"))\n",
    "\n",
    "            log.info(\"  → %s: %d rows written\", table, len(subset))\n",
    "            return len(subset)\n",
    "\n",
    "        # ------------------------------------------------------------------\n",
    "        # Readings hub\n",
    "        # ------------------------------------------------------------------\n",
    "        hub_cols = [\"reading_id\", \"location_id\", \"time\", \"data_source\", \"created_at\"]\n",
    "        incremental_load(\"readings\", hub_cols)\n",
    "\n",
    "        # ------------------------------------------------------------------\n",
    "        # Spoke tables (one per variable group)\n",
    "        # ------------------------------------------------------------------\n",
    "        incremental_load(\"particulates\", [\n",
    "            \"reading_id\", \"pm10\", \"pm2_5\", \"us_aqi\", \"aqi_category\",\n",
    "        ])\n",
    "        incremental_load(\"gaseous_pollutants\", [\n",
    "            \"reading_id\", \"carbon_monoxide\", \"nitrogen_dioxide\",\n",
    "            \"sulphur_dioxide\", \"ozone\",\n",
    "        ])\n",
    "        incremental_load(\"greenhouse_gases\", [\n",
    "            \"reading_id\", \"carbon_dioxide\", \"methane\",\n",
    "        ])\n",
    "        incremental_load(\"atmospheric\", [\n",
    "            \"reading_id\", \"uv_index\", \"uv_index_clear_sky\",\n",
    "            \"aerosol_optical_depth\", \"dust\",\n",
    "        ])\n",
    "\n",
    "        # ------------------------------------------------------------------\n",
    "        # Row count verification — confirm DB row count matches input\n",
    "        # ------------------------------------------------------------------\n",
    "        with engine.connect() as conn:\n",
    "            db_count = conn.execute(\n",
    "                text(\"SELECT COUNT(*) FROM readings WHERE data_source = 'api'\")\n",
    "            ).scalar()\n",
    "        log.info(\"  → Row count check: %d rows in DB readings table\", db_count)\n",
    "\n",
    "        # ------------------------------------------------------------------\n",
    "        # Audit log — record this pipeline run\n",
    "        # ------------------------------------------------------------------\n",
    "        with engine.begin() as conn:\n",
    "            conn.execute(text(\"\"\"\n",
    "                CREATE TABLE IF NOT EXISTS pipeline_runs (\n",
    "                    run_id          SERIAL PRIMARY KEY,\n",
    "                    started_at      TIMESTAMPTZ,\n",
    "                    completed_at    TIMESTAMPTZ,\n",
    "                    rows_extracted  INTEGER,\n",
    "                    db_row_count    INTEGER,\n",
    "                    status          TEXT\n",
    "                )\n",
    "            \"\"\"))\n",
    "            conn.execute(text(\"\"\"\n",
    "                INSERT INTO pipeline_runs\n",
    "                    (started_at, completed_at, rows_extracted, db_row_count, status)\n",
    "                VALUES\n",
    "                    (:started, :completed, :extracted, :db_count, 'SUCCESS')\n",
    "            \"\"\"), {\n",
    "                \"started\":   run_started_at,\n",
    "                \"completed\": datetime.now(timezone.utc),\n",
    "                \"extracted\": rows_in,\n",
    "                \"db_count\":  db_count,\n",
    "            })\n",
    "        log.info(\"  → Pipeline run recorded in pipeline_runs audit table\")\n",
    "\n",
    "    except Exception as e:\n",
    "        log.error(\"Database load failed: %s\", e)\n",
    "        # Record the failed run in the audit table if possible\n",
    "        try:\n",
    "            with engine.begin() as conn:\n",
    "                conn.execute(text(\"\"\"\n",
    "                    CREATE TABLE IF NOT EXISTS pipeline_runs (\n",
    "                        run_id SERIAL PRIMARY KEY, started_at TIMESTAMPTZ,\n",
    "                        completed_at TIMESTAMPTZ, rows_extracted INTEGER,\n",
    "                        db_row_count INTEGER, status TEXT\n",
    "                    )\n",
    "                \"\"\"))\n",
    "                conn.execute(text(\"\"\"\n",
    "                    INSERT INTO pipeline_runs\n",
    "                        (started_at, completed_at, rows_extracted, db_row_count, status)\n",
    "                    VALUES (:started, :completed, :extracted, NULL, 'FAILED')\n",
    "                \"\"\"), {\n",
    "                    \"started\":   run_started_at,\n",
    "                    \"completed\": datetime.now(timezone.utc),\n",
    "                    \"extracted\": rows_in,\n",
    "                })\n",
    "        except Exception as audit_err:\n",
    "            log.warning(\"Could not write failure record to pipeline_runs: %s\", audit_err)\n",
    "        raise\n",
    "\n",
    "\n",

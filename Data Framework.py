    "# ===========================================================================\n",
    "# STEP 6 — ANALYTICS-READY DATASETS\n",
    "    #Produce 3 files - \n",
    "    #1) Analytics Daily Summary - 1 row per location x date with mena and max per variable\n",
    "        #can load into Power Bi or Dash\n",
    "    #2) Analytics AQI Wide \n",
    "        #Pivoted Wide for timeseries analysis - 1 column per city indexed by time \n",

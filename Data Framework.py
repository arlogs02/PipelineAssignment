    "        df[col] = pd.to_numeric(df[col], errors=\"coerce\")\n",
    "\n",
    "    non_negative = [\n",
    "        \"pm10\", \"pm2_5\", \"carbon_monoxide\", \"carbon_dioxide\",\n",
    "        \"nitrogen_dioxide\", \"sulphur_dioxide\", \"ozone\", \"methane\",\n",
    "        \"uv_index\", \"uv_index_clear_sky\", \"dust\", \"aerosol_optical_depth\",\n",
    "        \"us_aqi\", \"us_aqi_pm2_5\", \"us_aqi_pm10\", \"us_aqi_nitrogen_dioxide\",\n",
    "        \"us_aqi_carbon_monoxide\", \"us_aqi_ozone\", \"us_aqi_sulphur_dioxide\",\n",
    "    ]\n",
    "    for col in non_negative:\n",
    "        if col in df.columns:\n",
    "            df[col] = df[col].clip(lower=0)\n",
    "\n",
    "    caps = {\"us_aqi\": 500, \"uv_index\": 20, \"aerosol_optical_depth\": 5}\n",
    "    for col, cap in caps.items():\n",
    "        if col in df.columns:\n",
    "            df[col] = df[col].clip(upper=cap)\n",
    "\n",
    "    measure_cols = [\n",
    "        \"pm10\", \"pm2_5\", \"carbon_monoxide\", \"carbon_dioxide\",\n",
    "        \"nitrogen_dioxide\", \"sulphur_dioxide\", \"ozone\", \"methane\",\n",
    "        \"uv_index\", \"dust\", \"aerosol_optical_depth\", \"us_aqi\",\n",
    "    ]\n",
    "    interp_frames = []\n",
    "    for _loc_id, grp in df.groupby(\"location_id\"):\n",
    "        grp = grp.sort_values(\"time\").copy()\n",
    "        for col in measure_cols:\n",
    "            if col in grp.columns:\n",
    "                grp[col] = grp[col].interpolate(method=\"linear\", limit=3)\n",
    "        interp_frames.append(grp)\n",
    "    df = pd.concat(interp_frames, ignore_index=True)\n",
    "\n",
    "    df = df.sort_values([\"location_id\", \"time\"]).reset_index(drop=True)\n",
    "    log.info(f\"  → {df.isnull().sum().sum():,} remaining nulls after interpolation\")\n",
    "    return df, loc\n",
    "\n",
    "\n",
    "# ===========================================================================\n",
    "# STEP 3 — TRANSFORMATION & DERIVED METRICS\n",
    "# - Add columns 1) AQI Cats (EPA AQI Cats), 2) Dom Poll - what is driving the AQI \n",
    "#3) PM Ratio - PM2.5/PM10 - Combustion or Dust (0-1) close to 0 | coarse = dust/pollen, close to 1 | fine = combustion\n",
    "#4) UV Cloud - % that UV is being surpressed by clouds, & 5)WHO Exceed - 0 within limit, 1 over\n",
    "#6) Date/hr/wk/day - time\n",

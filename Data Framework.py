    "# ===========================================================================\n",
    "def build_analytics_datasets(df: pd.DataFrame, locations_df: pd.DataFrame) -> None:\n",
    "\n",
    "    log.info(\"Building analytics-ready datasets …\")\n",
    "\n",
    "    try:\n",
    "        loc_name = locations_df.set_index(\"location_id\")[\"name\"].to_dict()\n",
    "        df = df.copy()\n",
    "        df[\"city\"] = df[\"location_id\"].map(loc_name)\n",
    "\n",
    "        # ------------------------------------------------------------------\n",
    "        # A) Daily summary\n",
    "        # ------------------------------------------------------------------\n",
    "        agg_map = {\n",
    "            \"pm2_5\":                  [\"mean\", \"max\"],\n",
    "            \"pm10\":                   [\"mean\", \"max\"],\n",
    "            \"ozone\":                  [\"mean\", \"max\"],\n",
    "            \"nitrogen_dioxide\":       [\"mean\", \"max\"],\n",
    "            \"sulphur_dioxide\":        [\"mean\", \"max\"],\n",
    "            \"carbon_monoxide\":        [\"mean\", \"max\"],\n",
    "            \"carbon_dioxide\":         [\"mean\"],\n",
    "            \"methane\":                [\"mean\"],\n",
    "            \"us_aqi\":                 [\"mean\", \"max\"],\n",
    "            \"uv_index\":               [\"max\"],\n",
    "            \"aerosol_optical_depth\":  [\"mean\"],\n",
    "            \"dust\":                   [\"mean\", \"max\"],\n",
    "            \"pm_ratio\":               [\"mean\"],\n",
    "            \"uv_cloud_attenuation_pct\": [\"mean\"],\n",
    "        }\n",
    "        agg_map = {k: v for k, v in agg_map.items() if k in df.columns}\n",
    "\n",
    "        daily = (\n",
    "            df.groupby([\"location_id\", \"city\", \"date\"])\n",
    "            .agg(agg_map)\n",
    "            .reset_index()\n",
    "        )\n",
    "        daily.columns = [\n",
    "            \"_\".join(c).strip(\"_\") if isinstance(c, tuple) else c\n",
    "            for c in daily.columns\n",
    "        ]\n",
    "\n",
    "        if \"dominant_pollutant\" in df.columns:\n",
    "            dominant = (\n",
    "                df.groupby([\"location_id\", \"date\"])[\"dominant_pollutant\"]\n",
